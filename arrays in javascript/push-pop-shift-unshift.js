// push and pop
let fruits =["kiwi", "guava", "mangoes", "grapes"];
console.log("previously", fruits)
fruits.push("banana");
console.log("now", fruits);
fruits.pop();
console.log("after pop",fruits)
// shift and unshift
fruits.unshift("banana");
console.log(fruits);// add one element from bigining
fruits.shift();
console.log(fruits); // removes one element from begining
